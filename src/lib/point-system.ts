// Simple fuzzy matching function
function normalizeName(name: string): string {
  return name
    .toLowerCase()
    .trim()
    .replace(/[^\w\s]/g, "") // Remove special chars
    .replace(/\s+/g, " "); // Normalize spaces
}

// Calculate similarity between two names (0-1)
function nameSimilarity(name1: string, name2: string): number {
  const n1 = normalizeName(name1);
  const n2 = normalizeName(name2);

  // Exact match
  if (n1 === n2) return 1.0;

  // Check if one contains the other
  if (n1.includes(n2) || n2.includes(n1)) {
    const shorter = Math.min(n1.length, n2.length);
    const longer = Math.max(n1.length, n2.length);
    return shorter / longer;
  }

  // Calculate Levenshtein-like similarity
  const longer = n1.length > n2.length ? n1 : n2;
  const shorter = n1.length > n2.length ? n2 : n1;
  const editDistance = levenshteinDistance(n1, n2);
  const maxLength = Math.max(n1.length, n2.length);

  return 1 - editDistance / maxLength;
}

// Simple Levenshtein distance
function levenshteinDistance(str1: string, str2: string): number {
  const matrix: number[][] = [];
  for (let i = 0; i <= str2.length; i++) {
    matrix[i] = [i];
  }
  for (let j = 0; j <= str1.length; j++) {
    matrix[0][j] = j;
  }
  for (let i = 1; i <= str2.length; i++) {
    for (let j = 1; j <= str1.length; j++) {
      if (str2.charAt(i - 1) === str1.charAt(j - 1)) {
        matrix[i][j] = matrix[i - 1][j - 1];
      } else {
        matrix[i][j] = Math.min(
          matrix[i - 1][j - 1] + 1,
          matrix[i][j - 1] + 1,
          matrix[i - 1][j] + 1
        );
      }
    }
  }
  return matrix[str2.length][str1.length];
}

// Find matching player from point system
export function findMatchingPlayer(playerName: string): {
  match: { name: string; points: number } | null;
  similarity: number;
} {
  let bestMatch: { name: string; points: number } | null = null;
  let bestSimilarity = 0;

  for (const player of PORT_SAID_PLAYERS) {
    const similarity = nameSimilarity(playerName, player.name);
    if (similarity > bestSimilarity) {
      bestSimilarity = similarity;
      bestMatch = player;
    }
  }

  // Threshold: 70% similarity or more
  if (bestSimilarity >= 0.7) {
    return { match: bestMatch, similarity: bestSimilarity };
  }

  return { match: null, similarity: bestSimilarity };
}



